class whileloop2
{
    public static void main(String [] args)
    {
        int n = 1;

        while (n<=100)

        {
            if (n%2 == 0)
            {
                System.out.println("SQL  "+n);
            }
            else
            {
                System.out.println("JAVA "+n);
            }

            n++;

        }




    }




}
